#include <bits/stdc++.h>

using namespace std;

int N,M,A[100010],B[100010];

int main()
{
	int T;
	scanf("%d",&T);
	while(T--)
	{
		scanf("%d%d",&N,&M);
		for (int i=1;i<=N;i++)
		{
			scanf("%d",&A[i]);
			B[i] = A[i];
		}
		while(M--)
		{
			int L1,R1,L2,R2;
			scanf("%d%d%d%d",&L1,&R1,&L2,&R2);
		}
	}
}
	
